#region 引用命名
using System;
using System.Runtime.CompilerServices;
#endregion

namespace Dt.Xls.Chart
{
    /// <summary>
    /// Represents additional color settings used in Chart
    /// </summary>
    public class ExcelDrawingColorSettings
    {
        /// <summary>
        /// Defines the additional Alpha value settings
        /// </summary>
        public double? Alpha { get; set; }

        /// <summary>
        /// Defines the additional Hue value settings
        /// </summary>
        public double? Hue { get; set; }

        /// <summary>
        /// Defines the additional HueMod value settings
        /// </summary>
        public double? HueMod { get; set; }

        /// <summary>
        /// Defines the additional HueOff value settings
        /// </summary>
        public double? HueOff { get; set; }

        /// <summary>
        /// Defines the additional Lum value settings
        /// </summary>
        public double? Lum { get; set; }

        /// <summary>
        /// Defines the additional LumMod value settings
        /// </summary>
        public double? LumMod { get; set; }

        /// <summary>
        /// Defines the additional LumOff value settings
        /// </summary>
        public double? LumOff { get; set; }

        /// <summary>
        /// Defines the additional Sat value settings
        /// </summary>
        public double? Sat { get; set; }

        /// <summary>
        /// Defines the additional SatMod value settings
        /// </summary>
        public double? SatMod { get; set; }

        /// <summary>
        /// Defines the additional SatOff value settings
        /// </summary>
        public double? SatOff { get; set; }

        /// <summary>
        /// Defines the additional Shade value settings
        /// </summary>
        public double? Shade { get; set; }

        /// <summary>
        /// Defines the additional Tint value settings
        /// </summary>
        public double? Tint { get; set; }
    }
}

