#region 引用命名
using System;
#endregion

namespace Dt.Cells.UI
{
    /// <summary>
    /// Represents the visual states used in the template of a <see cref="T:GrapeCity.Windows.SpreadSheet.UI.GcSpreadSheet" /> control
    /// and used to specify the elements' visual states in the visual tree of the control.
    /// </summary>
    public static class VisualStates
    {
        /// <summary>
        /// The active state group.
        /// </summary>
        public const string GroupActive = "ActiveStates";
        /// <summary>
        /// The common state group.
        /// </summary>
        public const string GroupCommon = "CommonStates";
        /// <summary>
        /// The current state group.
        /// </summary>
        public const string GroupCurrent = "CurrentStates";
        /// <summary>
        /// The focus state group.
        /// </summary>
        public const string GroupFocus = "FocusStates";
        /// <summary>
        /// The selection state group.
        /// </summary>
        public const string GroupSelected = "SelectionStates";
        /// <summary>
        /// The active state.
        /// </summary>
        public const string StateActive = "Active";
        /// <summary>
        /// The current state.
        /// </summary>
        public const string StateCurrent = "Current";
        /// <summary>
        /// The disabled state.
        /// </summary>
        public const string StateDisabled = "Disabled";
        /// <summary>
        /// The focused state.
        /// </summary>
        public const string StateFocused = "Focused";
        /// <summary>
        /// The inactive state.
        /// </summary>
        public const string StateInactive = "Inactive";
        /// <summary>
        /// The mouse over state.
        /// </summary>
        public const string StateMouseOver = "MouseOver";
        /// <summary>
        /// The normal state.
        /// </summary>
        public const string StateNormal = "Normal";
        /// <summary>
        /// The pressed state.
        /// </summary>
        public const string StatePressed = "Pressed";
        /// <summary>
        /// The regular state.
        /// </summary>
        public const string StateRegular = "Regular";
        /// <summary>
        /// The selected state.
        /// </summary>
        public const string StateSelected = "Selected";
        /// <summary>
        /// The unfocused state.
        /// </summary>
        public const string StateUnfocused = "Unfocused";
        /// <summary>
        /// The unselected state.
        /// </summary>
        public const string StateUnselected = "Unselected";
    }
}

